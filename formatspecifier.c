#include <stdio.h>
#include <stdbool.h>

int main(){

    float  item1 = 5.75;
    float item2 = 10.00;
    float item3 = 100.99;


    printf("Item 1 : $%8.2f\n", item1);  // you can specify the number of numbers of decimal place  with a "." before the the format specifier %2.f
    printf("Item 2 : $%8.3f\n", item2);  // %.3f
    printf("item 3 : $%8.4f\n", item3);  //%4.f





}